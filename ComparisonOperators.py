#Relational / Comparison Operators (==,!=, <, >, <=, >=)
a=50
b=20
equality= a==b
print(equality)#a is equal to b? False 
inequality= a!=b
print(inequality)# a is not equal to b? True
less= a<b
print(less)# a is less than b?False
greater= a>b
print(greater)#a is greater than b? True 
less_equal= a<=b
print(less_equal)# a is less than or equal to b? False
greater_equal= a>=b
print(greater_equal)#a is greater than or equal to b? True
